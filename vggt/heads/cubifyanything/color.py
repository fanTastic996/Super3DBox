def random_color(rgb=False, maximum=255):
    """
    Args:
        rgb (bool): whether to return RGB colors or BGR colors.
        maximum (int): either 255 or 1

    Returns:
        ndarray: a vector of 3 numbers
    """
    idx = np.random.randint(0, len(_COLORS))
    ret = _COLORS[idx] * maximum
    if not rgb:
        ret = ret[::-1]
    return ret

def random_color_v2(value, maximum=255):
    """
    输入0~1之间的数值，返回对应的RGB颜色（元组形式，范围0~1）
    参数:
        value (float): 归一化的数值，范围[0, 1]
    返回:
        tuple: (R, G, B) 颜色分量，每个值范围[0, 1]
    """
    # 获取jet colormap对象
    jet_cmap = cm.get_cmap('jet')
    # 将数值映射为RGBA（包含透明度）
    rgba = jet_cmap(value)
    # 提取RGB分量（忽略透明度Alpha）
    # return rgba[:3]#*maximum

    rgb = rgba[:3]   # 范围[0, maximum]
    return np.array(rgb) #* maximum # 转换为uint8数组